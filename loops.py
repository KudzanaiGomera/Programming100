# for loops - start to finish of an iterate

vegetables = ["cucumber", "spinach", "tomatoes", "cabage"]
for veggies in vegetables:
    print(veggies)

for i in range(10):
    print(i)

print("\n")
print("While loop\n")

# while loop - execute as long as True

i = 1
while (i < 10):
    print(i)
    i += 1